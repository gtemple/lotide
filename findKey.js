const findKey = function(obj, callback) {
  for (let i in obj) {
    if (callback(obj[i])) {
      return i;
    }
  }
}


let results1 = findKey({
  "Blue Hill": { stars: 1 },
  "Akaleri":   { stars: 3 },
  "noma":      { stars: 2 },
  "elBulli":   { stars: 3 },
  "Ora":       { stars: 2 },
  "Akelarre":  { stars: 3 }
}, x => x.stars === 2)

assertEqual(results1, 'noma')

